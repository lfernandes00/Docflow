package com.example.docflow;

public class NotificationDataModel {
    String desc, date;

    public NotificationDataModel(String desc, String date) {
        this.desc = desc;
        this.date = date;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc() {
        this.desc = desc;
    }

    public String getDate() {
        return date;
    }

    public void setDate() {
        this.date = date;
    }
}
